// Icon button wrapper
export function IconButton({ icon: Icon, label, onClick, disabled, className = '', variant = 'default', title }) {
  const base = 'flex flex-col items-center gap-1 px-3 py-2 rounded-lg transition-colors text-xs font-medium';
  const variants = {
    default: 'bg-gray-700 hover:bg-gray-600 text-white disabled:opacity-40',
    primary: 'bg-blue-600 hover:bg-blue-500 text-white disabled:opacity-40',
    danger: 'bg-red-600 hover:bg-red-500 text-white disabled:opacity-40',
    stop: 'bg-pink-800 hover:bg-pink-700 text-white disabled:opacity-40',
    ghost: 'text-gray-300 hover:text-white hover:bg-gray-700 disabled:opacity-40',
  };
  return (
    <button
      onClick={onClick}
      disabled={disabled}
      title={title || label}
      className={`${base} ${variants[variant] || variants.default} ${className}`}
    >
      <Icon className="w-5 h-5" />
      {label && <span>{label}</span>}
    </button>
  );
}
